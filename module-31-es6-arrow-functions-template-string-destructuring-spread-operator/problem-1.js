// an arrow function that will take 3 parameters, will multiply the parameters and will return the result.

let multiply3=(num1,num2,num3)=>num1*num2*num3;

console.log(multiply3(2,3,4));

// an arrow function that will take one parameter form user and another parameter will be default value and return their sum

let addDefault=(a,b=3)=>a+b;

console.log(addDefault(4));